	<h2>2012 Summer Olympics, London</h2>
	<p>When the 2012 Summer Olympic Games get underway in July, a handful of UCLA athletes will cross the pond to compete in London.</p>
	<p>While many national teams are still finalizing their Olympic rosters, the Bruins should live up to their reputation as strong performers at the Games.</p>
	<p>Since 1919, the school has sent 443 athletes to the Olympics, earning a total of 230 medals. Were UCLA its own country, this feat would place it 14th in the world in overall medal count.</p>
